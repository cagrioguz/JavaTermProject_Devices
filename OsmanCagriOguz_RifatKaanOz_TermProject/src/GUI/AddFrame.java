    private void nextbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextbtnActionPerformed
        if(constf.getText().equalsIgnoreCase("")||nametf.getText().equalsIgnoreCase("")||truebox.isSelected()||falsebox.isSelected()){
            if(w.isEmpty()){
                jTextArea1.setText("Please enter at least 1 website!");
            }
            else{
            if (typecb.getSelectedItem().toString().equalsIgnoreCase("Television")){
                deviceSys.addTv(yesbtn.isSelected(),w,nametf.getText(),truebox.isSelected(),Integer.parseInt(constf.getText()));
                w = new ArrayList<Website>();
                jTextArea1.setText("");
                nametf.setText("");
                constf.setText("");
                truebox.setSelected(false);
                falsebox.setSelected(false);
            }
            else if(typecb.getSelectedItem().toString().equalsIgnoreCase("Laptop")){
                deviceSys.addLaptop(Double.parseDouble(ramtf.getText()),Double.parseDouble(memorytf.getText()),gamepc.isSelected(),w,nametf.getText(),truebox.isSelected(),Integer.parseInt(constf.getText()));
                w = new ArrayList<Website>();
                jTextArea1.setText(""); 
                nametf.setText("");
                constf.setText("");
                truebox.setSelected(false);
                falsebox.setSelected(false);
            }
            else if(typecb.getSelectedItem().toString().equalsIgnoreCase("Refrigerator")){
                deviceSys.addRefrigerator(energytf.getText(),w,nametf.getText(),truebox.isSelected(),Integer.parseInt(constf.getText()));
                w = new ArrayList<Website>();
                jTextArea1.setText("");
                nametf.setText("");
                constf.setText("");
                truebox.setSelected(false);
                falsebox.setSelected(false);
            }
            else{
                jTextArea1.setText("Please select the device type!");
            }
                jTextArea1.setText("");
            }
            
        }
        else
             jTextArea1.setText("Please fill the necessary fields!");
       
    }//GEN-LAST:event_nextbtnActionPerformed

