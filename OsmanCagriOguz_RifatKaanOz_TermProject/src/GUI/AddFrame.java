    private void addwbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addwbtnActionPerformed
       
        if (webnametf.getText().isEmpty() || webpricetf.getText().isEmpty()) {
            jTextArea1.setText("Do not leave empty website field.");
        } else {
             String name = webnametf.getText();
            double price = Double.parseDouble(webpricetf.getText());
            w.add(new Website(name, price));
            jTextArea1.setText("Website is added!!");
            webnametf.setText("");
            webpricetf.setText("");
        }
    }//GEN-LAST:event_addwbtnActionPerformed

    private void typecbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typecbActionPerformed
        if(typecb.getSelectedItem().toString().equalsIgnoreCase("Television")){
            laptoppanel.setVisible(false);
            tvpanel.setVisible(true);
            refpanel.setVisible(false);
        }
        else if(typecb.getSelectedItem().toString().equalsIgnoreCase("Laptop")){
            laptoppanel.setVisible(true);
            tvpanel.setVisible(false);
            refpanel.setVisible(false);
        }
        else{
            laptoppanel.setVisible(false);
            tvpanel.setVisible(false);
            refpanel.setVisible(true);
        }
    }//GEN-LAST:event_typecbActionPerformed

